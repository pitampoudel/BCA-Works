//Qn.2) Write a program to add 2 values following different rules of identifier.

#include <stdio.h>
int main() {
    // Variables following different rules for identifiers
    int first_variable = 10;    // Identifier with underscores
    int FirstVariable = 20;    // CamelCase identifier
    int firstVariable999 = 30;  // Mixed case identifier with numbers

    // Perform addition
    int result = first_variable + FirstVariable + firstVariable999;

    // Display the result
    printf("The sum is: %d\n", result);

    return 0;
}













